namespace CoreEx.Json
{
    /// <summary>
    /// Defines the JSON serialization write format.
    /// </summary>
    public enum JsonWriteFormat
    {
        /// <summary>
        /// Indicates that no formatting is to occur.
        /// </summary>
        None,

        /// <summary>
        /// Indicates that pretty-printing indented formatting is to occur.
        /// </summary>
        Indented
    }
}